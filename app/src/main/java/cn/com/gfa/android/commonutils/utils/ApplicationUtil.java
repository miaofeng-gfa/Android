    private static Boolean mHasWebView;

    public static boolean hasWebView(Context context) {
        if (mHasWebView != null) {
            if (!mHasWebView)
                ToastUtil.show( "Not WebView for you phone",1000);
            return mHasWebView;
        }
        try {
            WebView webView = new WebView(context);
            webView.destroy();
            mHasWebView = true;
        } catch (Exception e) {
            e.printStackTrace();
            mHasWebView = false;
            ToastUtil.show("Not WebView for you phone",1000);
        }
        return mHasWebView;
    }


